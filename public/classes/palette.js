///// MAPPING COLOURS

function getComplementary(hexColor) {
    // Convert hex to RGB first
    let r = parseInt(hexColor.slice(1, 3), 16);
    let g = parseInt(hexColor.slice(3, 5), 16);
    let b = parseInt(hexColor.slice(5, 7), 16);

    // Find the complementary color
    r = 255 - r;
    g = 255 - g;
    b = 255 - b;

    // Convert RGB back to hex
    return "#" + ((1 << 24) + (r << 16) + (g << 8) + b).toString(16).slice(1);
}

function mapColors(obj, func) {
    for (let key in obj) {
        if (typeof obj[key] === "string" && obj[key].startsWith("#")) {
            obj[key] = func(obj[key]);
        } else if (Array.isArray(obj[key])) {
            obj[key] = obj[key].map(value => mapColors(value, func));
        } else if (typeof obj[key] === "object") {
            obj[key] = mapColors(obj[key], func);
        }
    }
    return obj;
}

function getComplementaryPalette(palette) {
    return mapColors(JSON.parse(JSON.stringify(palette)), getComplementary);
}

